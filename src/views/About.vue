I Fjärdhundra SK kan barn, ungdomar, juniorer och seniorer träna fler olika idrotter.

Fjärdhundra SK
Fjärdhundra Sport Klubb (FSK) bildades den 3 april 1986. Bildandet av FSK skedde på bekostnad av de två klubbarna Simtuna Sportklubb (bildad 1921) och Torstuna Sportklubb (bildad 1934), som båda gick i graven.

Bildandet skedde genom en sammanslagning av:
-Seniorfotbollen från Torstuna SK och Simtuna SK
-Ungdomsfotbollen från Torstuna SK, Simtuna SK och Härnevi BK (bildad 1922)
-Handbollen från Torstuna SK
-Skidorna från Torstuna SK

Fjärdhundra SK sysslade alltså från början med fotboll, handboll och skidor.

De fotbollslag som spelade säsongen 1986 kallades dock för Simtuna/Torstuna i seriespelet och inte Fjärdhundra SK, då FSK bildades senare än sista datumet för anmälan till serierna.

Klubbfärgerna blev en blandning mellan Simtuna SK:s och Torstuna SK:s tidigare klubbfärger. Simtuna hade använt rött och svart medan Torstuna hade grönt och svart. Som Fjärdhundra SK:s klubbfärger antogs grönt och rött samt att man lade till vitt till klubbmärket.

1997 bildades en fjärde sektion inom Fjärdhundra SK, nämligen innebandy.
